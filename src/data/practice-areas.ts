export const featuredPracticeAreas = practiceAreas;

export const primaryPracticeArea =
  practiceAreas.find((area) => area.featured) ?? practiceAreas[0];

export function getPracticeArea(slug: string) {
  return practiceAreas.find((area) => area.slug === slug);
}

export function getPracticeActivity(areaSlug: string, activitySlug: string) {
  const area = getPracticeArea(areaSlug);
  const activity = area?.highlights.find((item) => item.slug === activitySlug);

  if (!area || !activity) {
    return null;
  }

  return { area, activity };
}
